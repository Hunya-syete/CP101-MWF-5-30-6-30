First = float(input("Input 1: "))
Second = float(input("Input 2: "))
Operation = input("Add, Subtract, Multiply or Divide")

if Operation == "Add":
    Answer = First + Second
    
elif Operation == "Subtract":
    Answer = First - Second
    
elif Operation == "Multiply":
    Answer = First * Second
    
elif Operation == "Divide":
    Answer = First / Second
    
    
print(Answer)


