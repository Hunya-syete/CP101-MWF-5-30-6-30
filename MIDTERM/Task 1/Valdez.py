num1 = float(input("Enter First number: "))
num2 = float(input("Enter Second number: "))
operation = input("Choose operation (+, -, *, /): ")

if operation == "+":
    print(num1 + num2)
elif operation == "-":
    print(num1 - num2)
elif operation == "*":
    print(num1 * num2)
elif operation == "/":
    print(num1 / num2)
else:
    print("AYAY INVALID UYY")
